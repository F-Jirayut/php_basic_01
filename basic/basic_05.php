<?php

    // Comparison Operators
    // ==	Equal	$x == $y
    // ===	Identical	$x === $y
    // !=	Not equal	$x != $y
    // <>	Not equal	$x <> $y
    // !==	Not identical	$x !== $y
    // >	Greater than	$x > $y
    // <	Less than	$x < $y	
    // >=	Greater than or equal to	$x >= $y
    // <=	Less than or equal to	$x <= $y

    $num = 10;
    // using if
    if($num > 10){
        echo "num > 10";
        echo "<br>";
    }

    // using if else
    if($num < 10){
        echo "num > 10";
        echo "<br>";
    }else{
        echo "num is more 10";
        echo "<br>";
    }

    // using if else-if else
    if($num > 10){
        echo "num > 10";
        echo "<br>";
    }
    else if($num < 10){
        echo "num > 10";
        echo "<br>";
    }
    else{
        echo "num = 10";
        echo "<br>";
    }

    echo "<br>";
    echo "<a href=\"https://www.w3schools.com/php/php_if_else.asp\" target=\"_blank\">ดูเพิ่มเติม</a>";

?>